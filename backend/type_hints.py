# Type Hints

my_string_variable = 'My String variable'
print(my_string_variable)
print(type(my_string_variable))

my_string_variable = 4
print(my_string_variable)
print(type(my_string_variable))


my_typed_variable: str = 'My typed string variable'
print(my_typed_variable)

my_typed_variable = 5
print(my_typed_variable)


